def CD(a, b, box_len):
    dist2 = 0
    dim = len(a)
    for i in range(dim):
        dist = math.ceil(abs(float(a[i]) - float(b[i])) / box_len[i])
        dist2 += dist * dist
    return (1 - float(dist2) / float(dim + 1)) if dist2 < dim + 1 else 0

